"""
Load raw generator CSV and provide basic helpers.
"""

from pathlib import Path
from typing import Optional

import pandas as pd


def load_raw_dataset(
        path: str = "data/san_diego_energy_load_data.csv",
        parse_dates: Optional[list] = None,
) -> pd.DataFrame:
    """
    Load the raw CSV generated by the generator.
    Returns DataFrame with no index changes.
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"Raw data file not found: {path}")
    
    df = pd.read_csv(path)
    # Keep original columns; parsing of timestamp is left to feature engineering
    return df
